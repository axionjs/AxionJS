// Legacy export for backward compatibility
export const popularGoogleFonts = googleFonts.map((font) => font.name);

// Get available weights for a specific font
export const getAvailableWeights = (fontName: string): number[] => {
  const font = googleFonts.find((f) => f.name === fontName);
  return font ? font.weights : [400];
};

// Get font import URL
export const getFontImportUrl = (fontName: string): string => {
  const font = googleFonts.find((f) => f.name === fontName);
  return font ? font.importUrl : "";
};

// Get fonts by category
export const getFontsByCategory = (
  category: "sans-serif" | "serif" | "fancy" | "comic" | "display"
) => {
  return googleFonts.filter((font) => font.category === category);
};

// Get all fonts sorted alphabetically
export const getSortedFonts = () => {
  return [...googleFonts].sort((a, b) => a.name.localeCompare(b.name));
};

